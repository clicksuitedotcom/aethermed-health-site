function updateProcessDetail(widget, stepNumber) {
  const title = widget.querySelector("[data-process-title]");
  const copy = widget.querySelector("[data-process-copy]");
  const number = widget.querySelector(".process-detail-number");
  const visualImage = widget.querySelector("[data-process-image]");
  const bullets = widget.querySelector("[data-process-bullets]");
  const data = processStepData[stepNumber - 1];
  if (!data || !title || !copy || !number) return;

  title.setAttribute("data-i18n", data.title);
  copy.setAttribute("data-i18n", data.copy);
  title.textContent = translations[currentLanguage][data.title];
  copy.textContent = translations[currentLanguage][data.copy];
  number.textContent = String(stepNumber).padStart(2, "0");
  if (bullets) {
    const bulletKeys = processStepBullets[stepNumber] || [];
    bullets.innerHTML = bulletKeys.map((key) => `<li>${translations[currentLanguage][key]}</li>`).join("");
    bullets.hidden = bulletKeys.length === 0;
  }
  if (visualImage) {
    visualImage.src = processStepImages[stepNumber - 1];
    visualImage.dataset.stepImage = String(stepNumber);
  }

  widget.querySelectorAll(".process-step").forEach((button) => {
    const active = button.getAttribute("data-step") === String(stepNumber);
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-selected", String(active));
  });
}

document.querySelectorAll("[data-process-widget]").forEach((widget) => {
  widget.querySelectorAll(".process-step").forEach((button) => {
    button.addEventListener("click", () => {
      updateProcessDetail(widget, Number(button.getAttribute("data-step")));
    });
  });
});


function refreshProcessWidgets() {
  document.querySelectorAll("[data-process-widget]").forEach((widget) => {
    const active = widget.querySelector(".process-step.is-active")?.getAttribute("data-step") || "1";
    updateProcessDetail(widget, Number(active));
  });
}

document.addEventListener("DOMContentLoaded", refreshProcessWidgets);


