let currentLanguage = "en";
const toggle = document.querySelector(".language-toggle");
const languageMenu = document.querySelector(".language-menu");
const languageOption = document.querySelector("[data-language-option]");
const form = document.querySelector("#consultation-form");
const status = document.querySelector(".form-status");

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[language][key]) {
      element.textContent = translations[language][key];
    }
  });
  toggle.setAttribute("aria-label", language === "zh" ? "Switch language" : "切换语言");
  languageOption.textContent = language === "zh" ? "English" : "中文";
  languageOption.setAttribute("lang", language === "zh" ? "en" : "zh-CN");
  languageMenu.hidden = true;
  toggle.setAttribute("aria-expanded", "false");
  status.textContent = "";
  if (typeof refreshProcessWidgets === "function") refreshProcessWidgets();
}

toggle.addEventListener("click", () => {
  const isOpen = !languageMenu.hidden;
  languageMenu.hidden = isOpen;
  toggle.setAttribute("aria-expanded", String(!isOpen));
});

languageOption.addEventListener("click", () => {
  applyLanguage(currentLanguage === "zh" ? "en" : "zh");
});

document.addEventListener("click", (event) => {
  if (!event.target.closest(".language-picker")) {
    languageMenu.hidden = true;
    toggle.setAttribute("aria-expanded", "false");
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    languageMenu.hidden = true;
    toggle.setAttribute("aria-expanded", "false");
  }
});

form.addEventListener("submit", async (event) => {
  event.preventDefault();
  const submitButton = form.querySelector('button[type="submit"]');
  status.textContent = translations[currentLanguage].status_sending;
  if (submitButton) submitButton.disabled = true;

  try {
    const response = await fetch("/api/contact", {
      method: "POST",
      body: new FormData(form)
    });

    if (!response.ok) {
      throw new Error("Contact form request failed");
    }

    status.textContent = translations[currentLanguage].status_success;
    form.reset();
  } catch (error) {
    status.textContent = translations[currentLanguage].status_error;
  } finally {
    if (submitButton) submitButton.disabled = false;
  }
});


document.querySelectorAll("[data-network-tabs]").forEach((tabs) => {
  const buttons = tabs.querySelectorAll("[data-tab-target]");
  const panels = tabs.querySelectorAll(".tab-panel");

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      const targetId = button.getAttribute("data-tab-target");
      buttons.forEach((item) => {
        const active = item === button;
        item.classList.toggle("is-active", active);
        item.setAttribute("aria-selected", String(active));
      });
      panels.forEach((panel) => {
        const active = panel.id === targetId;
        panel.classList.toggle("is-active", active);
        panel.hidden = !active;
      });
      const activePanel = tabs.querySelector(`#${targetId}`);
      activePanel?.querySelectorAll(".carousel-shell").forEach((shell) => {
        window.requestAnimationFrame(() => updateCarouselDots(shell));
      });
    });
  });
});




function updateCarouselDots(shell) {
  const carousel = shell.querySelector(".network-carousel");
  const dots = shell.querySelectorAll(".carousel-dots span");
  const cards = carousel ? Array.from(carousel.querySelectorAll(".network-card")) : [];
  if (!carousel || !cards.length || !dots.length) return;

  const cardWidth = cards[0].getBoundingClientRect().width;
  const gap = parseFloat(getComputedStyle(carousel).columnGap || "16") || 16;
  const index = Math.max(0, Math.min(dots.length - 1, Math.round(carousel.scrollLeft / (cardWidth + gap))));
  dots.forEach((dot, dotIndex) => dot.classList.toggle("is-active", dotIndex === index));
}

document.querySelectorAll(".carousel-shell").forEach((shell) => {
  const carousel = shell.querySelector(".network-carousel");
  const prev = shell.querySelector("[data-carousel-prev]");
  const next = shell.querySelector("[data-carousel-next]");

  function scrollByCard(direction) {
    const card = carousel.querySelector(".network-card");
    if (!card) return;
    const gap = parseFloat(getComputedStyle(carousel).columnGap || "16") || 16;
    const cardWidth = card.getBoundingClientRect().width || carousel.clientWidth;
    const scrollDistance = Math.max(1, cardWidth + gap);
    carousel.scrollBy({ left: direction * scrollDistance, behavior: "smooth" });
    window.setTimeout(() => updateCarouselDots(shell), 360);
  }

  prev?.addEventListener("click", () => scrollByCard(-1));
  next?.addEventListener("click", () => scrollByCard(1));
  carousel?.addEventListener("scroll", () => updateCarouselDots(shell), { passive: true });
  updateCarouselDots(shell);
});


